//'Переданный параметр не является числом'.
//Если значение является числом, возвращает строку 
//'n в кубе равняется <получившееся значение>', где 
//n — введенное число, а <получившееся значение>
// — число, возведенное в куб.
//Проверьте работу функции с числами от 0 до 10.
function user() {
    let n = Number(prompt('Введите число'));
    if (!isNaN(n)) {
        let userNumber = Math.pow(n, 3);
        return `${n} в кубе равняется ${userNumber}`;
    } else {
        return 'Переданный параметр не является числом';
    }
    
}
