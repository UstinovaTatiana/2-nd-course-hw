//Проверяет, являются ли переданные параметры корректными числами.
//Подсказка: используйте преобразование типов и isNaN().
//Если нет — возвращает строку 
//'Одно или оба значения не являются числом'.
//Если оба параметра — числа, то возвращает произведение данных чисел.

function mult(c, d) {
    let x = Number(c);
    let y = Number(d);
if (!isNaN(x, y)) {
    return x * y;
}
else {
    return 'Одно или оба значения не явлются числом';
}
    
}
console.log(mult(2, 3));
console.log(mult('два', 3));
console.log(mult('4', 3));

